export function getExerciseById(id: string): Exercise | undefined {
  return EXERCISES.find(e => e.id === id);
}

export function getExercisesByEquipment(equipment: Equipment[]): Exercise[] {
  if (equipment.length === 0) return EXERCISES;
  return EXERCISES.filter(e => e.equipment.some(eq => equipment.includes(eq)));
}

export function getExercisesByMovement(pattern: MovementPattern): Exercise[] {
  return EXERCISES.filter(e => e.movementPattern === pattern);
}

export function getProgressionRoots(): Exercise[] {
  return EXERCISES.filter(e => !e.progressionParentId);
}

export function getProgressionTree(rootId: string): Exercise[] {
  const root = getExerciseById(rootId);
  if (!root) return [];
  const tree: Exercise[] = [root];
  const addChildren = (parentId: string) => {
    const parent = getExerciseById(parentId);
    if (parent?.progressionChildren) {
      for (const childId of parent.progressionChildren) {
        const child = getExerciseById(childId);
        if (child) {
          tree.push(child);
          addChildren(childId);
        }
      }
    }
  };
  addChildren(rootId);
  return tree;
}
